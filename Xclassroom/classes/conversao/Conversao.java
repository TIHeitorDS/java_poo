package Xclassroom.classes.conversao;

public class Conversao {
    public static double polegadasParaCentimetro(double polegadas) {
        return polegadas * 2.54;
    }

    public static double pesParaCentimetros(double pes) {
        return pes * 30.78;
    }

    public static double milhasParaQuilometros(double milhas) {
        return milhas * 1.609;
    }
}
